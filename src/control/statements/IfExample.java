package control.statements;

public class IfExample {  
	public static void main(String[] args) {  
		//defining an 'age' variable  
		int age=16;  
		//checking the age  
		if(age>18){  
			System.out.print("Age is greater than 18");  
		} 
		else
		{
			System.out.print("Age is less than 18"); 
		}
	}  
}  